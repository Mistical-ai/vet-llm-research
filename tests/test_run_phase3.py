def test_evaluate_dev_jsonl_deferred_papers_picked_up_next_run(
    _wired_paths, monkeypatch: pytest.MonkeyPatch,
) -> None:
    """Papers left out by the cap aren't lost -- a follow-up run judges the
    remainder, since they were never marked done."""
    import run_phase3

    source_dir, evals_dir = _wire_dev_jsonl(_wired_paths, monkeypatch, run_phase3)
    _stub_dev_eval_wiring(_wired_paths, monkeypatch, run_phase3)

    dois_by_journal = {
        "jvim": [f"10.9999/jvim.{i:04d}" for i in range(3)],
        "javma": [f"10.9999/javma.{i:04d}" for i in range(3)],
    }
    all_dois = [doi for dois in dois_by_journal.values() for doi in dois]
    _write_manifest_journals(_wired_paths["manifest_dir"] / "manifest.jsonl", dois_by_journal)
    _write_fake_summaries(_wired_paths["summaries_path"], _wired_paths["processed_dir"], all_dois)
    for doi in all_dois:
        _write_dev_readable_txt(source_dir, doi)

    result_1 = _run_evaluate(["evaluate", "--mode", "dev", "--limit", "3", "--force"])
    assert result_1 == 0
    manifest_1 = _find_manifest(_wired_paths["manifest_dir"])
    selected_1 = set(json.loads(manifest_1.read_text(encoding="utf-8"))["selected_instance_ids"])
    assert len(selected_1) == 3

    # The stubbed run_evaluation never appends real evaluations.jsonl rows,
    # so write_dev_eval_jsonl_outputs would write 0 files -- manually mark
    # this run's selection as done, standing in for what that function would
    # have written on a real (non-stubbed) run.
    for doi in selected_1:
        _write_dev_readable_txt(evals_dir, doi)

    result_2 = _run_evaluate(["evaluate", "--mode", "dev", "--limit", "3", "--force"])
    assert result_2 == 0
    # create_run_id() has 1-second granularity, so whether these two runs share
    # a manifest filename depends on wall-clock luck: land in the same second
    # and the second write overwrites the first, straddle a second boundary and
    # there are two files. Either way the run we want is the newest one, and
    # selected_1 was already captured in memory before this second call. (Do not
    # use _find_manifest here -- it asserts exactly one manifest exists.)
    manifest_2 = sorted(_wired_paths["manifest_dir"].glob("run_manifest_*.json"))[-1]
    selected_2 = set(json.loads(manifest_2.read_text(encoding="utf-8"))["selected_instance_ids"])

    assert selected_2.isdisjoint(selected_1)
    assert selected_1 | selected_2 == set(all_dois)


