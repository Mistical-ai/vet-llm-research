def _write_fake_summaries(summaries_path: Path, processed_dir: Path, dois: list[str]) -> None:
    """In plain English: writes fake/pretend summary data to disk that looks
    exactly like real output from `summarize`, but was never generated by
    an AI. This gives the tests below something realistic to evaluate/judge
    without making any real API call."""
    from file_paths import doi_to_slug

    processed_dir.mkdir(parents=True, exist_ok=True)
    with open(summaries_path, "w", encoding="utf-8") as f:
        for doi in dois:
            slug = doi_to_slug(doi)
            (processed_dir / f"{slug}.jsonl").write_text(
                json.dumps({"doi": doi, "slug": slug, "text": "Reference body. " * 30}) + "\n",
                encoding="utf-8",
            )
            f.write(json.dumps({
                "doi": doi,
                "custom_id": slug,
                "models": {
                    "openai": {
                        "status": "success",
                        "summary": f"summary for {doi}",
                        "model_version": "gpt-5.4-test",
                        "input_tokens": 100, "output_tokens": 50,
                    }
                },
            }) + "\n")


@pytest.fixture()
def _wired_paths(tmp_path: Path, monkeypatch: pytest.MonkeyPatch):
    """Redirect every module-level path constant this wiring touches into tmp_path.

    In plain English: this fixture makes sure that when a test runs, ALL file
    reading/writing happens inside a scratch/throwaway folder (`tmp_path`,
    created fresh by pytest for each test and deleted afterward) instead of
    the project's real data/ folder. That's what guarantees running these
    tests can never overwrite or corrupt real summaries.jsonl or
    evaluations.jsonl. Unlike the fixture above, this one isn't automatic —
    only tests that list `_wired_paths` as a parameter get it.
    """
    import evaluator
    import eval_report
    import eval_instances
    import prepare_texts
    import run_phase3
    import summarize_all_ingest

    summaries_path = tmp_path / "summaries.jsonl"
    evaluations_path = tmp_path / "evaluations.jsonl"
    processed_dir = tmp_path / "processed"

    monkeypatch.setattr(evaluator, "SUMMARIES_PATH", summaries_path)
    # The summarize-all input modes now judge the prose summary_text out of
    # summaries.jsonl rather than the bulleted .txt body, so this path has to
    # be redirected too or those tests would read the real project file.
    monkeypatch.setattr(summarize_all_ingest, "SUMMARIES_PATH", summaries_path)
    # cmd_evaluate's journal-stratified sampling branch (single/dev/batch,
    # anything other than --mode test) reads run_phase3's OWN module-level
    # SUMMARIES_PATH/MANIFEST_PATH directly — a separate pair of constants
    # from evaluator's, not a re-export — so patching only evaluator's above
    # leaves this branch reading the real project data/summaries.jsonl and
    # data/manifest.jsonl. --mode test never hits this branch (no journal
    # stratification there), which is why this gap went unnoticed until a
    # test exercised single/dev/batch's real sampling path.
    monkeypatch.setattr(run_phase3, "SUMMARIES_PATH", summaries_path)
    monkeypatch.setattr(run_phase3, "MANIFEST_PATH", tmp_path / "manifest.jsonl")
    monkeypatch.setattr(evaluator, "EVALUATIONS_PATH", evaluations_path)
    monkeypatch.setattr(evaluator, "PROCESSED_DIR", processed_dir)
    monkeypatch.setattr(prepare_texts, "PROCESSED_DIR", processed_dir)
    monkeypatch.setattr(eval_report, "EVALUATIONS_PATH", evaluations_path)
    monkeypatch.setattr(run_phase3, "RUN_MANIFEST_DIR", tmp_path)
    # write_dev_eval_jsonl_outputs/write_dev_detail_eval_outputs load the real
    # manifest by default when no manifest_index is passed in; point that at
    # tmp (nonexistent) paths too so tests never touch data/manifest.jsonl.
    monkeypatch.setattr(eval_instances, "MANIFEST_PATH", tmp_path / "manifest.jsonl")
    monkeypatch.setattr(eval_instances, "MANUAL_MANIFEST_PATH", tmp_path / "manual_manifest.jsonl")
    # _cmd_evaluate_from_txt_folder reads run_phase3.SUMMARIES_TXT_DIR /
    # DEV_TESTS_SUMMARIES_TXT_DIR -- real, unpatched project folders -- when
    # input_mode resolves to "regular"/"dev". That only happens if a test's
    # ambient EVAL_INPUT_MODE (from the developer's own .env) isn't "jsonl";
    # pin it here so this fixture's hermeticity guarantee holds regardless of
    # the machine it runs on. Any test that wants a different input mode
    # passes --input-mode explicitly, which always wins over this.
    monkeypatch.setenv("EVAL_INPUT_MODE", "jsonl")

    return {
        "summaries_path": summaries_path,
        "evaluations_path": evaluations_path,
        "processed_dir": processed_dir,
        "manifest_dir": tmp_path,
    }


def _run_evaluate(argv: list[str]):
    """In plain English: pretends to type `python llm-sum/run_phase3.py
    evaluate ...` by feeding a list of strings (argv, e.g.
    ["evaluate", "--mode", "test"]) through the SAME argparse parser
    run_phase3.py itself builds, then calls cmd_evaluate exactly the way the
    real CLI would. This is the standard way these tests drive the real code
    path instead of a simplified stand-in."""
    import run_phase3

    parser = run_phase3.build_parser()
    args = parser.parse_args(argv)
    return run_phase3.cmd_evaluate(args)


def _find_manifest(manifest_dir: Path) -> Path:
    """In plain English: looks in the scratch folder for the one
    run_manifest_*.json file that `evaluate` should have written, and fails
    the test immediately (with a clear message) if there isn't exactly one."""
    manifests = sorted(manifest_dir.glob("run_manifest_*.json"))
    assert len(manifests) == 1, f"expected exactly one manifest, found {manifests}"
    return manifests[0]


# In plain English: checks that running `evaluate --mode test` on two fake
# papers finishes successfully and writes a run-manifest record file that
# correctly lists both papers, the full 3-judge panel, and a "completed"
# status — the audit-trail record the real safety/reproducibility system
# depends on.
def test_evaluate_test_mode_writes_completed_run_manifest(_wired_paths) -> None:
    dois = ["10.9999/manifest.0001", "10.9999/manifest.0002"]
    _write_fake_summaries(_wired_paths["summaries_path"], _wired_paths["processed_dir"], dois)

    result = _run_evaluate(["evaluate", "--mode", "test"])

    assert result == 0
    manifest_path = _find_manifest(_wired_paths["manifest_dir"])
    data = json.loads(manifest_path.read_text(encoding="utf-8"))

    from evaluator import JURY_PANEL

    assert data["status"] == "completed"
    assert data["finished_utc"] is not None
    assert sorted(data["selected_instance_ids"]) == sorted(dois)
    # Default is now the full 3-judge panel.
    assert data["judges"] == JURY_PANEL
    assert all(j in data["model_ids"] for j in JURY_PANEL)
    # DRY_RUN mock mode returns "<model_id>-DRYRUN" as the judge's observed
    # version — this proves resolved_model_versions reads the real judge
    # response, not just echoing the configured model_ids. Every panel judge
    # should carry that mock marker.
    for judge in JURY_PANEL:
        assert data["resolved_model_versions"].get(judge, "").endswith("-DRYRUN")
    assert data["evaluation_config"]["mode"] == "test"
    taxonomy = data["evaluation_config"]["taxonomy"]
    assert taxonomy["taxonomy_id"] == "vet_taxonomy_v1"
    assert taxonomy["task_key"] == "veterinary_summary_quality"


