"""
Tests for the run-manifest wiring in llm-sum/run_phase3.py::cmd_evaluate().

These are wiring-level tests: they exercise the actual CLI entrypoint (via
the real argparse parser) in PHASE3_MODE=test (mock judge, $0, no live API
calls) and assert a run_manifest_*.json appears with the expected shape.

In plain English: this file is a set of automated checks that make sure the
command-line tool in llm-sum/run_phase3.py behaves correctly WITHOUT ever
calling a real, paid AI provider. Every test below runs entirely in
"--mode test", which is a safe mock mode built into the project: whenever a
test "calls" OpenAI/Anthropic/Gemini, it actually gets a fake, instant,
free response instead of a real one. That's what lets these tests run safely
and automatically (including here, via Claude) without risking any cost.
Each test function (the ones named `test_...`) sets up some fake data,
runs a piece of run_phase3.py's logic, and then checks ("asserts") that the
result looks the way it should.
"""

